    #include <stdio.h>

int main() {   // keyword
    printf("Hello,\nC Tokens!\n");
    return 0;   // keyword
}

// Keyword: คำสงวนในภาษา C เช่น int, return, main เป็นต้น

// Identifier: ชื่อที่ใช้เรียกตัวแปร ฟังก์ชัน หรือค่าสำคัญ เช่น main

// Constant: ค่าคงที่ เช่น 0, "Hello,", "C Tokens!"

// Operator: เครื่องหมายที่ใช้ในการคำนวณหรือการดำเนินการ เช่น +, -, *, =, , (เครื่องหมายคั่น)